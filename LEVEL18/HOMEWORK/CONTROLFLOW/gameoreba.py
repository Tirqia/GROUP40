# 1. ცვლადები და მონაცემთა ტიპები
სახელი = "ალეკო"  # სტრიქონი (string)
ასაკი = 16  # მთელი რიცხვი (int)
წონა = 55.5  # ათწილადი რიცხვი (float)
აქტიური = True  # ბულური ტიპი (bool)

# 2. მონაცემების შესაყვანად input() ფუნქცია
სახელი = input("შეიყვანე შენი სახელი: ")  # მიიღებს მომხმარებლის სახელს
print(f"გამარჯობა, {სახელი}!")  # გაიტანს შეტყობინებას, მომხმარებლის სახელით

# 3. მარტივი მათემატიკური ოპერაციები
a = 5
b = 3
print(a + b)  # 8
print(a - b)  # 2
print(a * b)  # 15
print(a / b)  # 1.666...
print(a == b)  # False, იმიტომ რომ a != b

# 4. პირობითი ოპერატორები (Control Flow - if, elif, else)
# ასაკის შემოწმება
if ასაკი < 18:
    print("თქვენ არასრულწლოვანი ხართ.")
elif ასაკი == 18:
    print("თქვენ სრულწლოვანი ხართ!")
else:
    print("თქვენ უკვე სრულწლოვანი ხართ.")

# 5. ლოგიკური ოპერაციები
x = 5
y = 10
if x < y and x != 0:
    print("x არის ნაკლები y-სა და არა 0.")
if not (x == y):  # ეს ნიშნავს რომ x != y
    print("x არ არის ისევე როგორც y.")

# 6.while loop
# for ციკლი
for i in range(5):  # გამოუშვებს 0-დან 4-მდე
    print(f"ნუმერი {i}")

# while loop
counter = 0
while counter < 3:
    print(f"counter: {counter}")
    counter += 1  # ყოველ ჯერზე ემატება 1

# 7.ცontrol Flow
# მომხმარებლის მიერ სახელი და ასაკის შეყვანა
